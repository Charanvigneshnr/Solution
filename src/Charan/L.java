package Charan;

public class L {
    static void LetterL(String s, int len)
    {
        for (int i = 0; i < len-1; i++)
        {
            System.out.println(s.charAt(i));
        }
        for (int i = len-1; i >= 0; i--)
        {
            System.out.print(s.charAt(i)+" ");
        }
    }
}



